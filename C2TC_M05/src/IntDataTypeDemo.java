//Primitive data Type

public class IntDataTypeDemo {

	public static void main(String[] args) {
		byte maxbyte=127;
		byte minbyte=-128;
		System.out.println("Max range of byte is "+maxbyte+" Min range of byte is "+minbyte);
		
		
		short maxshort=32767;
		short minshort=-32768;
		System.out.println("Max range of short is "+maxshort+" Min range of short is "+minshort);
	
		
		int maxint=2147483647;
		int minint=-2147483648;
		System.out.println("Max range of int is "+maxint+" Min range of int is "+minint);
		
		
		long maxlong=9223372036854775807L;
		long minlong=-9223372036854775808L;
		System.out.println("Max range of long is "+maxlong+" Min range of long is "+minlong);		
		
		
		float f =3.14324432322f;
		double d =3234.1432443232;
		System.out.println("float value is "+f+" double value is "+d);		


	}

}
